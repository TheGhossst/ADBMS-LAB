CREATE OR REPLACE PROCEDURE COUNT_EMP
IS
    CURSOR C IS SELECT DISTINCT(CNAME) FROM WORKS;
    COMPANY_NAME WORKS.CNAME%TYPE;
    COUNTS NUMBER := 0;
BEGIN
    OPEN C;
    LOOP
        FETCH C INTO COMPANY_NAME;
        EXIT WHEN C%NOTFOUND;

        SELECT COUNT(*) INTO COUNTS FROM WORKS WHERE CNAME = COMPANY_NAME;

        DBMS_OUTPUT.PUT_LINE(COMPANY_NAME || ' = ' || COUNTS);
    END LOOP;
    CLOSE C;
END;
/

BEGIN
    COUNT_EMP();
END;
/

CREATE OR REPLACE TRIGGER DELETE_EMP
BEFORE DELETE ON WORKS
FOR EACH ROW
DECLARE
    PRAGMA AUTONOMOUS_TRANSACTION;
    COUNT_EMP NUMBER;
BEGIN
    SELECT COUNT(*)
    INTO COUNT_EMP
    FROM WORKS
    WHERE CNAME = :OLD.CNAME;

    IF COUNT_EMP = 1 THEN
        RAISE_APPLICATION_ERROR(-20001, 'CANNOT DELETE LAST EMPLOYEE OF COMPANY');
    END IF;
END;
/

