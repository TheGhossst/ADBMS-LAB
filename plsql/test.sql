CREATE OR REPLACE TRIGGER BEFORE_DELETE_STUDENT
BEFORE DELETE
ON STUDENT
FOR EACH ROW
DECLARE
    PRAGMA AUTONOMOUS_TRANSACTION;
    COUNTS NUMBER;
BEGIN
    SELECT COUNT(*) INTO COUNTS FROM STUDENT
    WHERE DEPTID = :OLD.DEPTID;

    IF COUNTS = 1 THEN
        RAISE_APPLICATION_ERROR(-20001, 'CANNOT DELETE LAST STUDENT IN A DEPT');
    END IF;
END;
