CREATE OR REPLACE TRIGGER AFTER_UPDATE_COURSE
AFTER UPDATE
ON COURSE
FOR EACH ROW
BEGIN
    IF :OLD.CID != :NEW.CID THEN
        DBMS_OUTPUT.PUT_LINE('CID: ' || :OLD.CID || ' -> ' || :NEW.CID);
    END IF;

    IF :OLD.TITLE != :NEW.TITLE THEN
        DBMS_OUTPUT.PUT_LINE('TITLE: ' || :OLD.TITLE || ' -> ' || :NEW.TITLE);
    END IF;

    IF :OLD.CREDITS != :NEW.CREDITS THEN
        DBMS_OUTPUT.PUT_LINE('CREDITS: ' || :OLD.CREDITS || ' -> ' || :NEW.CREDITS);
    END IF;

    IF :OLD.DEPTID != :NEW.DEPTID THEN
        DBMS_OUTPUT.PUT_LINE('DEPTID: ' || :OLD.DEPTID || ' -> ' || :NEW.DEPTID);
    END IF;
END;
/

BEGIN
    INCREMENT_CREDIT(1);
END;
/


