CREATE OR REPLACE PROCEDURE COUNT_STUDENTS
IS
    CURSOR C IS SELECT DISTINCT(DEPTID) FROM STUDENT;
    DEPT STUDENT.DEPTID%TYPE;
    NAME DEPARTMENT.DEPTNAME%TYPE;
    COUNTS NUMBER := 0;
BEGIN
    OPEN C;
    LOOP
        FETCH C INTO DEPT;
        EXIT WHEN C%NOTFOUND;

        SELECT COUNT(*) INTO COUNTS FROM STUDENT
        WHERE DEPTID = DEPT;

        SELECT DEPTNAME INTO NAME FROM DEPARTMENT
        WHERE DEPTID = DEPT;

        DBMS_OUTPUT.PUT_LINE(NAME || ' = ' || COUNTS);
    END LOOP;
    CLOSE C;
END;
/

BEGIN
    COUNT_STUDENTS();
END;
/


CREATE OR REPLACE PROCEDURE LIST_COURSES(ID IN INT)
IS
    CURSOR C IS SELECT CID FROM ENROLLMENT WHERE SID = ID;
    COURSEID ENROLLMENT.CID%TYPE;
    COURSENAME COURSE.TITLE%TYPE;
BEGIN
    OPEN C;
    LOOP
        FETCH C INTO COURSEID;
        EXIT WHEN C%NOTFOUND;

        SELECT TITLE INTO COURSENAME FROM COURSE
         WHERE CID = COURSEID;

         DBMS_OUTPUT.PUT_LINE(COURSENAME);

    END LOOP;

    CLOSE C;
END;
/

