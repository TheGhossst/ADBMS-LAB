CREATE TABLE DOCTOR(
    D_ID INT PRIMARY KEY,
    NAME VARCHAR2(29),
    SPECIALIZATION VARCHAR2(29)
)

CREATE TABLE PATIENT(
    P_ID INT PRIMARY KEY,
    NAME VARCHAR2(20),
    L_ID INT,
    FOREIGN KEY (L_ID) REFERENCES LOCATION(L_ID)
)

CREATE TABLE TREATS(
    D_ID INT,
    P_ID INT,
    TREATMENT_ID INT,
    TREATMENT_DATE DATE,
    PRIMARY KEY (D_ID, P_ID, TREATMENT_ID),
    FOREIGN KEY (D_ID) REFERENCES DOCTOR(D_ID),
    FOREIGN KEY (P_ID) REFERENCES PATIENT(P_ID)
)

CREATE TABLE LOCATION(
    L_ID INT PRIMARY KEY,
    ROOM_TYPE VARCHAR2(5),
    NAME VARCHAR2(29)
)

CREATE TABLE NURSE(
    N_ID INT,
    NAME VARCHAR2(20),
    SHIFT_DUTY VARCHAR2(20),
    D_ID INT,
    L_ID INT,
    PRIMARY KEY (N_ID, L_ID),
    FOREIGN KEY (D_ID) REFERENCES DOCTOR(D_ID),
    FOREIGN KEY (L_ID) REFERENCES LOCATION(L_ID)
);

