-- Number of patients assisted by each nurse for every doctor

SELECT N.N_ID, N.D_ID, COUNT(DISTINCT T.P_ID)
FROM NURSE N
JOIN TREATS T ON N.D_ID = T.D_ID
GROUP BY N.N_ID, N.D_ID;

