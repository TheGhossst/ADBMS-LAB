--Doctor who treated maximum patients

SELECT D_ID, COUNT(DISTINCT P_ID)
FROM TREATS
GROUP BY D_ID
HAVING COUNT(DISTINCT P_ID) = (
    SELECT MAX(cnt)
    FROM (
        SELECT COUNT(DISTINCT P_ID) AS cnt
        FROM TREATS
        GROUP BY D_ID
    )
);

-- Patients not treated at all
SELECT P_id
FROM PATIENT P
WHERE NOT EXISTS (
    SELECT * FROM TREATS T
    WHERE T.P_id = P.P_id
);


